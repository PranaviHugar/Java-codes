public class ifelseif
{
    public static void main(String args[])
    {   int x=30;
        if(x<1)
        {System.out.println("infant");}
        else if(x<10)
        {System.out.println("kid");}
        else if(x<18)
        {System.out.println("teenager");}
        else if(x<30)
        {System.out.println("youth");}
        else
        {System.out.println("adult");}
    }
}